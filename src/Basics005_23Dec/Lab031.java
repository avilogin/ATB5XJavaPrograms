package src.Basics005_23Dec;

public class Lab031 {
    public static void main(String[] args) {

        // Find odd  number within 1-50

        for (int i = 0;i<=50;i++)
        {
            if (i%2==1)
                System.out.println(i+" is a Odd number");
        }

        System.out.println("---------------------------------");
        // Find Even  number within 1-50

        for (int i = 0;i<=50;i++)
        {
            if (i%2==0)
                System.out.println(i+" is a Even number");
        }








    }
}
